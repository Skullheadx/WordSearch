def search_string(s):
    output = []
    for word in word_list:
        if word in s:
            output.append(word)
    return output


def find_words(b):
    words = []

    for col in col_search_pattern:
        s = ""
        for coord in col:
            s += b[coord].letter
        words.append(search_string(s))
        words.append(search_string(s[::-1]))

    for row in row_search_pattern:
        s = ""
        for coord in row:
            s += b[coord].letter
        words.append(search_string(s))
        words.append(search_string(s[::-1]))

    for diag in l_diagonal_search_pattern:
        s = ""
        for coord in diag:
            s += b[coord].letter
        words.append(search_string(s))
        words.append(search_string(s[::-1]))

    for diag in r_diagonal_search_pattern:
        s = ""
        for coord in diag:
            s += b[coord].letter
        words.append(search_string(s))
        words.append(search_string(s[::-1]))

    return words


