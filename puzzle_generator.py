class Tile:

    def __init__(self, x, y, letter):
        self.letter = letter
        self.neighbors = set()
        for i in range(-1, 2):
            for j in range(-1, 2):
                if not (i == 0 and j == 0):
                    tx, ty = x + i, y + j
                    if (0 <= tx < height) and (0 <= ty < length):
                        self.neighbors.add((tx, ty))


